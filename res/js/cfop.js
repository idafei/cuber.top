function cfopAlg() {

	var algs = cfopAlgs;

	this.initAlgs = function() {
    $('.cfop-algs').each(function(i) {
      var key = $(this).data('id');
      var alg = algs[key];
      $(this).children('.name').text(alg.name);
      // $(this).children('.pic').html('<img src="/res/images/cfop/' + key + '.jpg">');
      $(this).children('.algs').html(alg.algs.join('<br>'));
    });
	};

};
